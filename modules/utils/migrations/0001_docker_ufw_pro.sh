# 0001_docker_ufw_pro.sh - Обновление Docker Firewall до Professional Fix
#
# ══════════════════════════════════════════════════════════════════════════
# 🛠 ШАБЛОН МИГРАЦИИ (Reshala Patch Standard)
# ══════════════════════════════════════════════════════════════════════════
# Каждая миграция — это независимый модуль, который загружается движком.
# Движок вызывает migration_check() для проверки и migration_apply() для дела.
# ══════════════════════════════════════════════════════════════════════════
# ══════════════════════════════════════════════════════════════════════════
# 📖 ПРАВИЛА ДЛЯ РАЗРАБОТЧИКОВ (КАК ДОБАВИТЬ МИГРАЦИЮ):
# ══════════════════════════════════════════════════════════════════════════
# 1. Создайте новый файл в папке modules/utils/migrations/ (напр. 0002_fix.sh).
# 2. В файле обязательно должны быть определены:
#    - MIG_ID="УНИКАЛЬНЫЙ_ID"
#    - MIG_TITLE="Краткий заголовок патча"
#    - MIG_DESC="Подробное описание для пользователя"
# 3. Реализуйте две функции:
#    - migration_check() - должна вернуть 0, если миграция НУЖНА.
#    - migration_apply() - логика применения. Возвращает 0 при успехе.
# 4. Движок сам подхватит файл, проверит его и предложит пользователю.
# ══════════════════════════════════════════════════════════════════════════
MIG_ID="DOCKER_UFW_PRO_V1"
MIG_TITLE="Обновление Docker Firewall (Professional Fix)"
MIG_DESC="Включает систему маршрутизации трафика через ufw-user-forward для полного контроля над контейнерами."

# Функция проверки: возвращает 0 если миграция НУЖНА
migration_check() {
    # 1. Если уже применяли — не нужно
    is_migration_applied "$MIG_ID" && return 1
    
    # 2. Условие: Docker стоит, UFW активен
    if command -v docker &>/dev/null && command -v ufw &>/dev/null; then
        if ufw status | grep -q "active"; then
            # 3. Если в after.rules старый фикс или его нет (нет ufw-user-forward)
            if ! grep -q "ufw-user-forward" /etc/ufw/after.rules 2>/dev/null; then
                return 0
            fi
        fi
    fi
    return 1
}

# Функция применения
migration_apply() {
    info "Запуск обновления Docker UFW Fix..."
    
    # Модуль firewall.sh должен быть загружен движком перед вызовом
    if command -v _firewall_fix_docker_ufw &>/dev/null; then
        _firewall_fix_docker_ufw
        return 0
    else
        err "Модуль Firewall не найден. Не могу применить фикс."
        return 1
    fi
}
