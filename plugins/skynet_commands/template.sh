#

# --- Стандартные хелперы для плагинов Skynet ---
# Эти хелперы обеспечивают консистентный вывод и обработку ошибок.
# Они не зависят от common.sh, чтобы плагины были автономными.
set -e # Прерывать выполнение при любой ошибке
C_RESET='\033[0m'; C_RED='\033[0;31m'; C_GREEN='\033[0;32m'; C_YELLOW='\033[1;33m';
info() { echo -e "${C_RESET}[i] $*${C_RESET}"; }
ok()   { echo -e "${C_GREEN}[✓] $*${C_RESET}"; }
warn() { echo -e "${C_YELLOW}[!] $*${C_RESET}"; }
err()  { echo -e "${C_RED}[✗] $*${C_RESET}"; exit 1; }
# --- Конец хелперов ---

# --- Главная логика плагина ---

# Убедимся, что скрипт выполняется от имени root, т.к. Skynet запускает его через sudo.
if [[ $EUID -ne 0 ]]; then
    err "Этот плагин должен выполняться от имени root."
fi

info "Запуск плагина..."

#
# ... здесь основная логика вашего плагина ...
#
# Пример:
# ok "Проверка диска завершена."
# warn "Место почти закончилось."
#

ok "Плагин успешно выполнен."
exit 0
